package Task1_Squares;

public class Area {
    public int getTriangleArea(int h, int base) {
        return (h * base) / 2;
    }

    public int getRectangleArea(int a, int b) {
        return a * b;
    }

    public int getSquareArea(int a) {
        return a * a;
    }

    public int getRhombusArea(int p, int q) {
        return (p * q) / 2;
    }
}
